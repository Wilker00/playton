import numpy as np
class Dog:
    def __init__(self, name, age):
        self.name = name  # Initializes the 'name' attribute with the value passed in
        self.age = age    # Initializes the 'age' attribute with the value passed in
my_dog = Dog("Buddy", 5)
print(my_dog.name)  # Output: Buddy
print(my_dog.age)   # Output: 5

